/*!
****************************************************************************************************
* 功能描述：该函数用于判断本地是否为大端字节序
* 注意事项：NA
* 输入参数：NA
* 输出参数：NA
* 返回参数：如果本地是大端字节序返回TRUE，否则返回FALSE
****************************************************************************************************
*/
bool Endian_isBigEndian(void)
{
	static const unsigned int endian = 0x01;

	unsigned char* p = (unsigned char*)&endian;

	if (p[0] == 0x01) {
		return false;
	}

	return true;
}

